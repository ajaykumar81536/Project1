#Q2. declaring variables of types : string, int, float, bool, set , frozenset, dict, list, complex

# String
my_string = "Hello, World!"

# Integer
my_int = 42

# Float
my_float = 3.14

# Boolean
my_bool = True

# Set
my_set = {1, 2, 3, 4, 5}
listt = [1,2,2,3,3,4]
newset = set(listt)
print(newset)

# Frozenset
my_frozenset = frozenset({1, 2, 3, 4, 5})

# Dictionary
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}

# List
my_list = [1, 2, 3, 4, 5]

# Complex
my_complex = 2 + 3j

# Print the variables
print("String:", my_string)
print("Integer:", my_int)
print("Float:", my_float)
print("Boolean:", my_bool)
print("Set:", my_set)
print("Frozenset:", my_frozenset)
print("Dictionary:", my_dict)
print("List:", my_list)
print("Complex:", my_complex)
