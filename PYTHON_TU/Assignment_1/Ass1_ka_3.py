# Q3. Unpacking a collection from a list.

# Create a list
my_list = [1, 2, 3, 4, 5]

# Unpack the list into individual variables
first, second, third, fourth, fifth = my_list


# Print the unpacked variables
print("First:", first)
print("Second:", second)
print("Third:", third)
print("Fourth:", fourth)
print("Fifth:", fifth)
